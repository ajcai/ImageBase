//内部函数
BOOL CMainFrame::CreateDockingWindows()
{
	BOOL bNameValid;
	 //创建缩略图窗口
	CString strOutputWnd;
	bNameValid = strOutputWnd.LoadString(IDS_THUMBNAIL_STR);
	ASSERT(bNameValid);
	if (!m_wndOutput.Create(strOutputWnd, this, CRect(0, 0, 100, 100), TRUE, ID_VIEW_THUMBNAIL, WS_CHILD | WS_VISIBLE | WS_CLIPSIBLINGS | WS_CLIPCHILDREN | CBRS_BOTTOM | CBRS_FLOAT_MULTI)){
		TRACE0("未能创建输出窗口\n");
		return FALSE; // 未能创建
	}
	m_wndOutput.EnableDocking(CBRS_ALIGN_ANY);
	DockPane(&m_wndOutput);
	
	// 创建属性窗口
	CString strPropertiesWnd;
	bNameValid = strPropertiesWnd.LoadString(IDS_PROPERTIES_STR);
	ASSERT(bNameValid);
	if (!m_wndProperties.Create(strPropertiesWnd, this, CRect(0, 0, 200, 200), TRUE, 400, WS_CHILD | WS_VISIBLE | WS_CLIPSIBLINGS | WS_CLIPCHILDREN | CBRS_RIGHT | CBRS_FLOAT_MULTI))
	{
		TRACE0("未能创建“属性”窗口\n");
		return FALSE; // 未能创建
	}
	m_wndProperties.SetControlBarStyle(~AFX_CBRS_CLOSE);//去掉属性窗口的关闭按钮，禁止用户关闭
	m_wndProperties.EnableDocking(CBRS_ALIGN_ANY);
	DockPane(&m_wndProperties);

	return TRUE;
}

