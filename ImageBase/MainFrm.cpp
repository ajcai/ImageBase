void CMainFrame::ChangeProcessBar(bool isRun)
{
	if(isRun){
		//重绘状态栏，显示进度条
		m_pProgressBar->SetVisible(TRUE);
		m_wndStatusBar.RecalcLayout();
		m_pProgressBar->Redraw();
		//定时进度
		UINT ELAPSETIME=800;	//间隔时间
		this->SetTimer(ID_PROCESSBAR_TIMER,ELAPSETIME,NULL);
	}else{
		//重绘状态栏，隐藏进度条
		m_pProgressBar->SetVisible(FALSE);
		m_wndStatusBar.RecalcLayout();
		m_wndStatusBar.Invalidate();
		m_wndStatusBar.UpdateWindow();
		//取消定时器
		this->KillTimer(ID_PROCESSBAR_TIMER);
	}
}


